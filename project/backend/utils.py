def get_ai_cure_assistance(disease: str) -> dict:
    """
    AI Agent function to provide cure assistance and product recommendations
    for detected diseases.

    Args:
        disease: The predicted disease name

    Returns:
        Dictionary containing:
        - cure_advice: List of actionable cure/treatment advice
        - products: List of product recommendations with links
        - when_to_see_doctor: Guidance on when to seek medical help
        - ai_note: Disclaimer about AI-generated advice
    """
    # Normalize disease name for matching
    disease_stripped = disease.strip()
    disease_lower = disease_stripped.lower()
    
    # Build a case-insensitive lookup dictionary
    disease_lookup = {k.lower().strip(): v for k, v in DISEASE_CURE_DATABASE.items()}
    
    # Try exact match first (case-insensitive)
    cure_info = disease_lookup.get(disease_lower, None)
    
    # If not found, try exact match with original case (handles trailing spaces)
    if cure_info is None:
        cure_info = DISEASE_CURE_DATABASE.get(disease_stripped, None)
    
    # Try matching with trailing space (some diseases have trailing spaces like "Diabetes ")
    if cure_info is None:
        cure_info = DISEASE_CURE_DATABASE.get(disease_stripped + " ", None)
        if cure_info is None:
            cure_info = disease_lookup.get(disease_lower + " ", None)
    
    # If not found, try partial matching
    if cure_info is None:
        # Common words to exclude from matching
        stop_words = {'disease', 'infection', 'disorder', 'syndrome', 'unknown', 'the', 'and', 'or'}
        
        for db_disease, info in DISEASE_CURE_DATABASE.items():
            db_lower = db_disease.lower().strip()
            # Check if one contains the other (but skip very short input)
            if len(disease_lower) > 4:
                if db_lower in disease_lower or disease_lower in db_lower:
                    cure_info = info
                    break
            # Check for key word matching (e.g., "Fungal" matches "Fungal infection")
            disease_words = set(disease_lower.replace("_", " ").split()) - stop_words
            db_words = set(db_lower.replace("_", " ").split()) - stop_words
            if disease_words & db_words:  # If there's word overlap
                common_words = disease_words & db_words
                # Require at least one significant word match (not just short words)
                if any(len(w) > 4 for w in common_words):
                    cure_info = info
                    break
    
    # If still not found, use default advice
    if cure_info is None:
        cure_info = DEFAULT_CURE_INFO.copy()
    
    return {
        "cure_advice": cure_info.get("cure_advice", DEFAULT_CURE_INFO["cure_advice"]),
        "products": cure_info.get("products", DEFAULT_CURE_INFO["products"]),
        "when_to_see_doctor": cure_info.get("when_to_see_doctor", DEFAULT_CURE_INFO["when_to_see_doctor"]),
        "ai_note": "⚠️ This advice is AI-generated for educational purposes only. Always consult a qualified healthcare professional before starting any treatment."
    }
