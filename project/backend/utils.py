def format_prediction_result(disease: str, confidence: float, method: str = "symptom") -> dict:
    return {
        "disease": disease,
        "confidence": round(confidence, 2),
        "method": method,
        "severity": _severity_label(confidence),
        "description": get_disease_description(disease),
        "precautions": get_disease_precautions(disease),
    }


def _severity_label(confidence: float) -> str:
    if confidence >= 85:
        return "High confidence — consult a doctor"
    if confidence >= 65:
        return "Moderate confidence — monitoring recommended"
    return "Low confidence — more tests needed"


