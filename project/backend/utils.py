def preprocess_image(filepath: str, target_size: tuple = IMAGE_SIZE) -> np.ndarray:
    """
    Load, resize, normalise to [0, 1], add batch dimension.
    Returns shape ``(1, 224, 224, 3)``.
    """
    img = Image.open(filepath).convert("RGB")
    img = img.resize(target_size, Image.LANCZOS)
    arr = np.array(img, dtype=np.float32) / 255.0
    return np.expand_dims(arr, axis=0)


def _extract_simple_features(filepath: str) -> np.ndarray:
    """
    Simple sklearn-compatible feature extraction without torch.
    Extracts color histograms, statistics, and texture features.
    Returns a 1-D float32 array of shape (1280,) for compatibility.
    """
    img = Image.open(filepath).convert("RGB")
    img = img.resize((224, 224), Image.LANCZOS)
    arr = np.array(img, dtype=np.float32) / 255.0
    img.close()
    
    features = []
    
    # Per-channel statistics (3 channels × 4 stats = 12)
    for c in range(3):
        ch = arr[:, :, c]
        features.extend([ch.mean(), ch.std(), ch.min(), ch.max()])
    
    # Color histograms (3 channels × 64 bins = 192)
    for c in range(3):
        hist, _ = np.histogram(arr[:, :, c].flatten(), bins=64, range=(0, 1))
        features.extend(hist / hist.sum())
    
    # Spatial grid features (8×8 grid, 3 channels = 192)
    for c in range(3):
        grid = arr[:, :, c].reshape(8, 28, 8, 28).mean(axis=(1, 3))
        features.extend(grid.flatten())
    
    # Edge detection approximation using gradients (2 directions × 64 = 128)
    gray = arr.mean(axis=2)
    gx = np.abs(np.diff(gray, axis=1)).mean(axis=0)
    gy = np.abs(np.diff(gray, axis=0)).mean(axis=1)
    # Resample to 64 values each
    features.extend(np.interp(np.linspace(0, len(gx)-1, 64), np.arange(len(gx)), gx))
    features.extend(np.interp(np.linspace(0, len(gy)-1, 64), np.arange(len(gy)), gy))
    
    # Pad or truncate to 1280 dimensions for compatibility
    features = np.array(features, dtype=np.float32)
    if len(features) < 1280:
        features = np.pad(features, (0, 1280 - len(features)), mode='constant')
    else:
        features = features[:1280]
    
    return features


def extract_image_features(filepath: str) -> np.ndarray:
    """
    Extract CNN features from a single image using pretrained EfficientNet-B0.
    Returns a 1-D float32 array of shape (1280,).
    Falls back to simple features if torch is not available.
    """
    try:
        import torch
        from torchvision import models, transforms
        import torch.nn as nn
    except ImportError:
        # Torch not available - use simple feature extraction
        return _extract_simple_features(filepath)

    global _CNN_MODEL, _CNN_TRANSFORM
    try:
        _CNN_MODEL  # already loaded
    except NameError:
        _CNN_MODEL = None
        _CNN_TRANSFORM = None

    if _CNN_MODEL is None:
        _cnn = models.efficientnet_b0(weights=models.EfficientNet_B0_Weights.DEFAULT)
        _cnn.classifier = nn.Identity()

        # Load fine-tuned backbone weights if available (blocks 7-8 adapted)
        _ft_path = os.path.join(BASE_DIR, "finetuned_backbone.pth")
        if os.path.exists(_ft_path):
            _cnn.load_state_dict(torch.load(_ft_path, map_location="cpu"))

        _cnn.eval()
        for p in _cnn.parameters():
            p.requires_grad = False
        _CNN_MODEL = _cnn
        _CNN_TRANSFORM = transforms.Compose([
            transforms.Resize((224, 224)),
            transforms.ToTensor(),
            transforms.Normalize([0.485, 0.456, 0.406], [0.229, 0.224, 0.225]),
        ])

    img = Image.open(filepath).convert("RGB")
    try:
        # Light test-time augmentation: original + horizontal flip.
        imgs = [img, img.transpose(Image.FLIP_LEFT_RIGHT)]
        tensors = torch.stack([_CNN_TRANSFORM(im) for im in imgs], dim=0)
        with torch.inference_mode():
            feats = _CNN_MODEL(tensors)
        # Average pooled features across views
        feats = feats.mean(dim=0)
        return feats.squeeze().cpu().numpy()
    finally:
        img.close()


def validate_medical_image(filepath: str) -> tuple[bool, str]:
    """Basic sanity checks on the uploaded image."""
    try:
        img = Image.open(filepath)
        w, h = img.size
        if w < 50 or h < 50:
            return False, "Image too small (min 50 × 50 px)."
        if w > 10_000 or h > 10_000:
            return False, "Image too large (max 10 000 × 10 000 px)."
        return True, "Valid image."
    except Exception as exc:
        return False, f"Cannot process image: {exc}"


def augment_image(img_array: np.ndarray) -> list[np.ndarray]:
    """Lightweight augmentations for a single (H, W, 3) array."""
    return [
        img_array,
        np.fliplr(img_array),
        np.clip(img_array * 1.2, 0, 1),
        np.clip(img_array * 0.8, 0, 1),
    ]


