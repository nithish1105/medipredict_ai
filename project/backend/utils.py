def get_symptom_list() -> list[str]:
    """Return the canonical symptom list used during training."""
    return SYMPTOM_LIST


def get_image_classes() -> list[str]:
    """Return the ordered image class list used during training."""
    return IMAGE_CLASSES


def encode_symptoms(selected_symptoms: list[str]) -> np.ndarray:
    """
    Encode selected symptoms into a numeric feature vector of length
    ``len(SYMPTOM_LIST)``.  Values are severity weights (or 1 if unknown).
    """
    vector = np.zeros(len(SYMPTOM_LIST), dtype=np.float32)
    sym_to_idx = {s: i for i, s in enumerate(SYMPTOM_LIST)}

    for symptom in selected_symptoms:
        clean = symptom.strip().lower().replace(" ", "_").replace("__", "_")
        if clean in sym_to_idx:
            weight = SEVERITY_DICT.get(clean, 1)
            vector[sym_to_idx[clean]] = weight
    return vector


def get_disease_description(disease: str) -> str:
    """Return a textual description for the disease, or empty string."""
    return DESCRIPTION_DICT.get(disease, "")


def get_disease_precautions(disease: str) -> list[str]:
    """Return a list of precaution strings for the disease."""
    return PRECAUTION_DICT.get(disease, [])


